    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (jtxtDescripcion.getText().trim().isEmpty() || jtxtPrecio.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Llenar campos requeridos");

        } else {

            Categoria_Habitacion ch = new Categoria_Habitacion();
            ch.setDescripcion(jtxtDescripcion.getText());
            ch.setPrecio(Double.parseDouble(jtxtPrecio.getText()));
            cc.insertar(ch);
            //Para actualizar la tabla
            dt.setRowCount(0);
            cc.ListadoCategoria(dt);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

