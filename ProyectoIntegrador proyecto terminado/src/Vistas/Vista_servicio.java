    void generarnropedido() {
        String nropedido = servicio.nroPedido();
        if (nropedido == null) {
            jtxtcodigosh.setText("000001");
        } else {
            int increment = Integer.parseInt(nropedido);
            increment = increment + 1;
            jtxtcodigosh.setText("00000" + increment);
        }
    }
 
