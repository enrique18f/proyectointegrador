    void muestra2(Integer cod){
      tpagar=0;
      dt=(DefaultTableModel)tabledetalle.getModel();
      dt.setRowCount(0);
//       tpagar = 0;
      for(Detalle_servicio x:detalle.ListadoDet(cod)){
          Object v[]={x.getId_detalle(),x.getCodigo_sh(),x.getId_produto(),x.getNombre_producto(),x.getPreciopro(),x.getCantidad(),x.getTotal()};
          tpagar = tpagar + x.getTotal();
          jtxttotal.setText("" + tpagar + "0");
          dt.addRow(v);
         
      }
  }
    
