    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int codigo = Integer.parseInt(this.jtxtID.getText());
        cc.Anular(codigo);
        //Para actualizar la tabla
        dt.setRowCount(0);
        cc.ListadoEmpleado(dt);

        //Limpiar entradas
        this.jtxtID.setText("");
        this.jtxtNombre.setText("");
        this.jtxtApellido.setText("");
        this.jtxtUsuario.setText("");
        this.jtxtContraseña.setText("");
        this.jtxtCargo.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Empleado o = new Empleado();
        o.setNombre(jtxtNombre.getText());
        o.setApellido(jtxtApellido.getText());
        o.setUsuario(jtxtUsuario.getText());
        o.setContraseña(jtxtContraseña.getText());
        o.setCargo(jtxtCargo.getText());
        cc.insertar(o);
        //Para actualizar la tabla
        dt.setRowCount(0);
        cc.ListadoEmpleado(dt);

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        if(jtxtNombre.getText().trim().isEmpty() || jtxtApellido.getText().trim().isEmpty() ||  jtxtUsuario.getText().trim().isEmpty() || jtxtContraseña.getText().trim().isEmpty() || jtxtCargo.getText().trim().isEmpty()){

            JOptionPane.showMessageDialog(null, "Llenar campos requeridos");

        } else {
            Empleado o = new Empleado();
            o.setId_empleado(Integer.parseInt(this.jtxtID.getText()));
            o.setNombre(this.jtxtNombre.getText());
            o.setApellido(this.jtxtApellido.getText());
            o.setUsuario(this.jtxtUsuario.getText());
            o.setContraseña(this.jtxtContraseña.getText());
            o.setCargo(this.jtxtCargo.getText());
            cc.Actualizar(o);
        }
        //Para actualizar la tabla
        dt.setRowCount(0);
        cc.ListadoEmpleado(dt);
    }//GEN-LAST:event_btnActualizarActionPerformed

