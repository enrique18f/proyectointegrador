    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        if (jtblEmpleados.getSelectedRow() >= 0) {
            this.btnGuardar.setEnabled(false);
            this.btnCancelar.setEnabled(true);
            this.btnActualizar.setEnabled(true);
            this.btnEliminar.setEnabled(true);
            int filaseleccionada = jtblEmpleados.getSelectedRow();

            int id = Integer.parseInt(this.jtblEmpleados.getValueAt(filaseleccionada, 0).toString());

            Empleado o = cc.ConsultarPorID(id);

            this.jtxtID.setText(Integer.toString(o.getId_empleado()));
            this.jtxtNombre.setText(o.getNombre());
            this.jtxtApellido.setText(o.getApellido());
            this.jtxtUsuario.setText(o.getUsuario());
            this.jtxtContraseña.setText(o.getContraseña());
            this.jtxtCargo.setText(o.getCargo());

        }else{
            JOptionPane.showMessageDialog(null, "Selecciona un empleado de la tabla");
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

